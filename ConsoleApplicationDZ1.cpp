void printMatrix(int** array, const size_t SIZE)
{
	for (size_t i = 0; i < SIZE; i++)
	{
		for (size_t j = 0; j < SIZE; j++)
		{
			cout << array[i][j] << " ";
		}
		cout << endl;
	}

}
void initMatrix(int** pMatrix, const size_t SIZE)
{
	for (size_t i = 0; i < SIZE; i++)
	{
		for (size_t j = 0; j < SIZE; j++)
		{
			pMatrix[i][j] = rand() % 50;
		}
	}

}
