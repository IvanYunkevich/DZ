void printArray(const int* Array, const size_t arrSize)
{

	cout << "Array: " << "\n";
	for (size_t i = 0; i < arrSize; i++)
	{
		cout << Array[i] << " " << endl;
	}


}
void initArray(int* arr, const size_t size)
{
	arr[0] = 1;
	for (size_t i = 1; i < size; i++)
	{
		arr[i] = arr[i - 1] << 1;
	}

}
